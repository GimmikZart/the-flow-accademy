      <p class="side-content-description" >VIDEO DANCE: ESPRESSIVITÀ ED ENERGIA.
        <br><br>
        La videodance  è un genere che nasce in America durante gli anni ’70 dalla fusione di vari stili, quali jazz, hip-hop e modern. Questo stile di danza si identifica prettamente nei Videoclip musicali.
        <br><br>
        Il Videoclip, infatti, è uno strumento promozionale utilizzato soprattutto dai cantanti che, accompagnati da ballerini professionisti, ballano sotto la guida di coreografi e registi.
        <br><br>
        Lo stile della Video Dance non ha regole da seguire. Lo si può ballare da soli, in due o in gruppo proprio come vediamo nei videoclip. Tuttavia esso è caratterizzato da movimenti molto schematici, supportati da sistemi computerizzati che rendono le coreografie ancora più particolari ed affascinanti.
        <br><br>
        Attraverso questo stile di danza il ballerino apprenderà le tecniche specifiche per diventare un vero e proprio performer, aiutandolo a prendere confidenza con il proprio movimento e a lavorare con le telecamere. </p>
