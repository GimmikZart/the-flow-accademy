    <h2 class="side-content-title">Popping</h2>
    <p class="side-content-description" >Il Popping è uno stile di danza funk e danza hip hop basato sulla tecnica della rapida contrazione e successivo rilassamento dei muscoli, che causa una sorta di scatto nel corpo del ballerino, chiamato pop (schiocco) o hit (colpo). Queste contrazioni sono eseguite continuamente al ritmo di musica in combinazione con altri stili mimici.[1]

    Il Popping è anche utilizzato come termine ombrello sotto cui ricadono diversi stili e tecniche di danza che sono spesso combinate al vero e proprio popping per creare una esibizione maggiormente varia. L'effetto del popping si dimostra particolarmente surrealistico quando accosta il ballerino umano ai movimenti innaturali che vengono eseguiti. Come altre danze di strada, il popping è eseguito di solito all'interno di battle e sviluppa movimenti atti ad irridere l'avversario.

    Generalmente si fa risalire la nascita del popping negli anni 1970 in California, originariamente ispirato al locking.[2] Questa danza fu successivamente incorporata nella scena delle danze hip hop e elettroniche.</p>
