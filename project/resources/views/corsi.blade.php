    <h2 class="side-content-title">Locking</h2>
    <p class="side-content-description" > (originalmente “Campbellocking”), è una danza Hip Hop sviluppatasi alla fine degli anni 60 a Los Angeles. Prevede movimenti rigidi e veloci in cui vengono toccate le varie parti del corpo. I movimenti distinti e rapidi delle braccia, si abbinano ad altrettanti movimenti fluidi e molleggiati delle gambe, eseguiti su movimenti funk. Si basa sul concetto di “lock”: eseguire movimenti veloci per poi bloccarli a distanza. Con la sua gestualità e la sua mimica, riesce a catturare bene l’attenzione del pubblico circostante, esprimendo senza dubbio padronanza e controllo di se. Il locker (colui o colei che danza) è spesso caratterizzato da un abbigliamento stravagante, con calzettoni a righe e cappelli vistosi. La paternità del locking è dello statunitense “Don Campbellock”; alla fine degli anni 60 egli combinò assieme vari movimenti presi da danze d’intrattenimento (fad dances) per creare originali performance. Il locking si sviluppò durante gli anni 70 quando Don Cambellock fondò il gruppo “The Lockers” ed entrò in contatto con la cultura Hip Hop, diventandone protagonista.(Fa parte dell’Old School”);</p>
