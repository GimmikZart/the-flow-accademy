      <h2 class="side-content-title">HipHop</h2>
      <p class="side-content-description" >La danza hip hop è una disciplina di ballo, considerata anche "danza sportiva" dalla F.ID.S. (Federazione Italiana Danza Sportiva) e dalla I.D.O. (International Dance Organization) e altre federazioni di danza minori, che proviene dal movimento Hip Hop e ne conserva le caratteristiche culturali ed estetiche. Le principali tecniche della danza di strada (street dance) sono il b-boying (breakdance), il locking, il popping e l'uprock. La prima è considerata l'originale danza di strada, poiché è nata assieme alle altre discipline dell'Hip Hop (Dj'ing, Writing, Mc'ing). Le altre vengono considerate eguali al b-boying solo da alcuni, poiché sono danze nate nello stesso periodo ma in luoghi diversi. Recentemente si sono sviluppati nuovi stili di danza come il New style, il Krumping e l'House che non sono accettati da tutti come danze hip hop, poiché si sono sviluppati in contesti culturali differenti da quelli originali. Di queste discipline la breakdance è la più conosciuta ed è considerata una vera e propria pietra miliare della cultura hip hop.</p>
